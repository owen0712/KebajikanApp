const mongoose = require('mongoose');
const {ObjectId} = mongoose.Schema.Types;

const charityApplicationSchema = new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    phone_number:{
        type:String,
        required:true
    },
    identity_no:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    ic_no:{
        type:String,
        required:true
    },
    marital_status:{
        type:String,
        required:true
    },
    current_address:{
        type:String,
        required:true
    },
    permanent_address:{
        type:String,
        required:true
    },
    program:{
        type:String,
        required:true
    },
    department:{
        type:String,
        required:true
    },
    year_of_study:{
        type:Integer,
        required:true
    },
    semester:{
        type:Integer,
        required:true
    },
    father_occ:{
        type:String,
        required:true
    },
    mother_occ:{
        type:String,
        required:true
    },
    father_income:{
        type:Double,
        required:true
    },
    mother_income:{
        type:Double,
        required:true
    },
    total_income:{
        type:Double,
        required:true
    },
    no_sibling:{
        type:Integer,
        required:true
    },
    no_dependent:{
        type:Integer,
        required:true
    },
    status:{
        type:String,
        required:true,
        default:"Pending"
    },
    created_by:{
        type:ObjectId,
        ref:"User",
        required:true
    },
    crated_on:{
        type:Date,
        default:Date.now,
        required:true
    },
    verified_by:{
        type:ObjectId,
        required:true
    },
    verified_on:{
        type:Date,
        required:true
    },
    document:{

    }
})

mongoose.model('CharityApplication',charityApplicationSchema);